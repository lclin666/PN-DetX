class ConvNextBlock(nn.Module):
 
    def __init__(self, inputdim, dim, drop_path=0., layer_scale_init_value=1e-6,
                 kersize=7):  # demo: [64, 64, 1]  1 denotes the number of repeats
        super().__init__()
